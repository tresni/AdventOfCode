def feedback_loop(instructions, phases):
    initial = None
    previous = None
    thread = None
    for phase in phases:
        processor = IntCode(
            list(instructions),
            previous.output if previous else None)
        processor.input.put(phase)
        thread = Thread(target=processor.operate)
        thread.start()

        if initial == None:
            initial = processor
        previous = processor
    previous.output = initial.input
    initial.input.put(0)
    thread.join()
    return initial.input.get()

